/**
 * Per-ERP deterministic column rules for ledger PDFs.
 *
 * Ledger exports come from a small number of accounting packages
 * (Tally, Busy, Marg, Finsys) that each have a stable, recognisable
 * page layout AND extract cleanly through the deterministic grid
 * extractor. When fingerprinted we can map columns to roles from a
 * per-ERP header→role table and skip the wizard — same playbook as
 * `perBankRules.ts` does for HDFC / ICICI / Canara.
 *
 * Mirrors `perBankRules.ts`. If grid extraction worked but no rule
 * fingerprint matches, OR a required role is missing after mapping,
 * we return null — the caller falls back to the interactive wizard.
 *
 * The four formats we currently support, with their distinguishing
 * tells:
 *
 *   Busy        — single-letter "B" voucher-type column (J / C / P),
 *                 "Totals C/F" / "Totals B/F" carry-forward markers,
 *                 dd-mm-yyyy dates. All-caps account-name section
 *                 headers between transaction blocks.
 *
 *   Marg ERP    — dd.mm.yyyy dates (the dotted format is almost
 *                 unique to Marg in the Indian ERP market), "Balance
 *                 c/f:" / "Balance b/f:" with lowercase + colon,
 *                 "Page X of Y" pagination, account names with a
 *                 leading dash ("-HDFC BANK LTD.").
 *
 *   Tally       — month-abbrev + day dates ("Apr 1", "Jun 30") with
 *                 the year only on the first row, "Carried Over" /
 *                 "Continued..N" page-rollover markers, amounts
 *                 without Indian comma grouping.
 *
 *   Finsys ERP  — "Generated By Finsys ERP" page footer, "Ledger
 *                 Code :" banner, "Totals & Balance" subtotal label.
 *                 Each transaction occupies two visual rows: row 1
 *                 has the date in the date column, row 2 has the
 *                 voucher number (which fails parseDate). The
 *                 existing applyMapping continuation-merge handles
 *                 the row-2 join automatically — the row-2 narration
 *                 / amount cells get appended to the row-1 transaction.
 */

import { inferFiscalYearStart, parseDate, type ColumnMapping, type ColumnRole, type PdfGrid } from './pdfGrid';

interface ErpRule {
  /** Display name surfaced in the success toast and console logs. */
  name: string;
  /** Lowercase substrings searched for in the joined text of the
   *  first ~100 grid rows. Any match counts as a positive
   *  fingerprint hit. We scan further than the bank rule (30 rows)
   *  because dense ledger pages (40+ transactions) push page-footer
   *  markers like "Generated By Finsys" / "Carried Over" past the
   *  first 30 rows. */
  fingerprints: string[];
  /** Optional grid-reshape hook. Runs AFTER the fingerprint matches
   *  but BEFORE headerRules / required checks. Use this when an ERP's
   *  compact PDF layout merges two logical columns into one grid
   *  cell — Tally's "Vch No. Debit" merged header (Hotel Holiday Inn
   *  2024-25 export) is the canonical case: the Vch No and the Debit
   *  amount land in the same column as bareint, "<bareint> <currency>",
   *  or "<currency> Dr/Cr" cells depending on row type. Mirrors
   *  `preprocess` in perBankRules.ts; return a new PdfGrid with the
   *  split applied or the input grid unchanged to skip. */
  preprocess?: (grid: PdfGrid) => PdfGrid;
  /** Header → role table. Iterated in order for each grid column;
   *  first matching pattern wins. List specific patterns first. */
  headerRules: Array<{ pattern: RegExp; role: ColumnRole }>;
  /** Roles that MUST be present after mapping for the rule to fire.
   *  Missing any one means grid extraction didn't surface the full
   *  table — bail and fall back to the wizard. */
  required: ColumnRole[];
}

